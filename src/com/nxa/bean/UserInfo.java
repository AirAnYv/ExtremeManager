package com.nxa.bean;

public class UserInfo {
	
	private Integer uiID;
	private String uiName;
	private String uiPassword;
	private Integer uiRole;
	private String uiRealname;
	private String uiTelephone;
	public Integer getUiID() {
		return uiID;
	}
	public void setUiID(Integer uiID) {
		this.uiID = uiID;
	}
	public String getUiName() {
		return uiName;
	}
	public void setUiName(String uiName) {
		this.uiName = uiName;
	}
	public String getUiPassword() {
		return uiPassword;
	}
	public void setUiPassword(String uiPassword) {
		this.uiPassword = uiPassword;
	}
	public Integer getUiRole() {
		return uiRole;
	}
	public void setUiRole(Integer uiRole) {
		this.uiRole = uiRole;
	}
	public String getUiRealname() {
		return uiRealname;
	}
	public void setUiRealname(String uiRealname) {
		this.uiRealname = uiRealname;
	}
	public String getUiTelephone() {
		return uiTelephone;
	}
	public void setUiTelephone(String uiTelephone) {
		this.uiTelephone = uiTelephone;
	}
	
	
	

}
